#include "libft.h"
char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == c % 256)
			return ((char *)s);
		s++;
	}
	if (c % 256 == '\0')
		return ((char *)s);
	return (NULL);
}
